/* Compiler — chars to a tinted, outlined, 1px-per-pixel canvas         */
/* ------------------------------------------------------------------ */

export interface Rect { x: number; y: number; w: number; h: number }

export interface CompiledFrame {
  /** ART_W x ART_H canvas, one canvas pixel per art pixel. Pupils excluded. */
  img: HTMLCanvasElement;
  /** art-x of the feet, so a tail sticking out one side doesn't shift the cat */
  anchorX: number;
  /** one per eye, in draw order; pupils are painted at runtime so they can track */
  pupils: Rect[];
  /** eye-white bounding boxes, used to close the lids for a blink */
  eyes: Rect[];
}

const SILHOUETTE = new Set(['K', 'S', 'B', 'E', 'P', 'N', 'I']);

const CHAR_COLOR: Record<string, keyof CoatPalette> = {
  K: 'body', S: 'shade', B: 'belly', E: 'eye', P: 'eye', N: 'nose', I: 'earInner', w: 'detail',
};

/** 8-connected clusters of eye pixels, so left and right eyes stay independent */
function eyeClusters(rows: string[]): { cells: [number, number][] }[] {
  const seen = new Set<string>();
  const out: { cells: [number, number][] }[] = [];
  for (let y = 0; y < ART_H; y++) {
    for (let x = 0; x < ART_W; x++) {
      const ch = rows[y][x];
      if (ch !== 'E' && ch !== 'P') continue;
      const key = `${x},${y}`;
      if (seen.has(key)) continue;
      const cells: [number, number][] = [];
      const stack: [number, number][] = [[x, y]];
      seen.add(key);
      while (stack.length) {
        const [cx, cy] = stack.pop()!;
        cells.push([cx, cy]);
        for (let dy = -1; dy <= 1; dy++) {
          for (let dx = -1; dx <= 1; dx++) {
            const nx = cx + dx, ny = cy + dy;
            if (nx < 0 || nx >= ART_W || ny < 0 || ny >= ART_H) continue;
            const nch = rows[ny][nx];
            if (nch !== 'E' && nch !== 'P') continue;
            const nkey = `${nx},${ny}`;
            if (seen.has(nkey)) continue;
            seen.add(nkey);
            stack.push([nx, ny]);
          }
        }
      }
      out.push({ cells });
    }
  }
  return out;
}

function bbox(cells: [number, number][]): Rect {
  let x0 = Infinity, y0 = Infinity, x1 = -Infinity, y1 = -Infinity;
  for (const [x, y] of cells) {
    if (x < x0) x0 = x; if (x > x1) x1 = x;
    if (y < y0) y0 = y; if (y > y1) y1 = y;
  }
  return { x: x0, y: y0, w: x1 - x0 + 1, h: y1 - y0 + 1 };
}

function compileFrame(art: { anchorX: number; rows: string[] }, pal: CoatPalette): CompiledFrame {
  const cv = document.createElement('canvas');
  cv.width = ART_W;
  cv.height = ART_H;
  const ctx = cv.getContext('2d')!;
  const rows = art.rows;

  // 1. outline first — every transparent pixel touching the silhouette
  ctx.fillStyle = pal.outline;
  for (let y = 0; y < ART_H; y++) {
    for (let x = 0; x < ART_W; x++) {
      if (rows[y][x] !== '.') continue;
      let touching = false;
      for (let dy = -1; dy <= 1 && !touching; dy++) {
        for (let dx = -1; dx <= 1; dx++) {
          const nx = x + dx, ny = y + dy;
          if (nx < 0 || nx >= ART_W || ny < 0 || ny >= ART_H) continue;
          if (SILHOUETTE.has(rows[ny][nx])) { touching = true; break; }
        }
      }
      if (touching) ctx.fillRect(x, y, 1, 1);
    }
  }

  // 2. the cat itself. Pupils are laid down as eye-white; the renderer paints
  //    the real pupil on top wherever the cat happens to be looking.
  for (let y = 0; y < ART_H; y++) {
    for (let x = 0; x < ART_W; x++) {
      const ch = rows[y][x];
      if (ch === '.') continue;
      const key = CHAR_COLOR[ch];
      if (!key) continue;
      ctx.fillStyle = pal[key];
      ctx.fillRect(x, y, 1, 1);
    }
  }

  const clusters = eyeClusters(rows);
  const eyes: Rect[] = [];
  const pupils: Rect[] = [];
  for (const c of clusters) {
    eyes.push(bbox(c.cells));
    const pupilCells = c.cells.filter(([x, y]) => rows[y][x] === 'P');
    if (pupilCells.length) pupils.push(bbox(pupilCells));
  }

  return { img: cv, anchorX: art.anchorX, pupils, eyes };
}

export type Sheet = Record<string, CompiledFrame>;

const sheetCache = new Map<number, Sheet>();

/** Compiled frames for a coat. Baked once, then reused for the session. */
export function getSheet(coatIndex: number): Sheet | null {
  if (typeof document === 'undefined') return null; // SSR — no canvas to bake into
  const i = ((coatIndex % COATS.length) + COATS.length) % COATS.length;
  const cached = sheetCache.get(i);
  if (cached) return cached;
  const pal = COATS[i];
  const sheet: Sheet = {};
  for (const [name, art] of Object.entries(FRAME_ART)) sheet[name] = compileFrame(art, pal);
  sheetCache.set(i, sheet);
  return sheet;
}
