/**
 * The Canvas Resident's sprite sheet.
 *
 * Every pose is hand-authored pixel art on a fixed 40x32 grid, stored as one
 * character per pixel. Nothing here is computed from the simulation — which is
 * the whole point. The previous cat drew itself from raw fillRect calls at
 * fractional coordinates, so legs changed height mid-stride and the silhouette
 * tore apart at certain zooms. Baked frames can't stretch, can't tear, and a
 * walk cycle either reads as a cat or it doesn't while you're still drawing it.
 *
 * Character legend:
 *   .  transparent      K  body            S  shade (far limbs, depth)
 *   B  belly            E  eye white       P  pupil (drawn at runtime, so the
 *   N  nose             I  inner ear          cat can look at your cursor)
 *   w  light detail — whiskers, closed-eye lines, the seam between two
 *      overlapping forms. Deliberately NOT part of the silhouette, so it
 *      never grows an outline of its own.
 *
 * The outline is never authored. It's dilated from the silhouette at compile
 * time, which means it is always sealed and always exactly one pixel — the
 * single detail that separates "desktop companion" from "blob with ears".
 */

export interface CoatPalette {
  name: string;
  body: string;
  shade: string;
  belly: string;
  outline: string;
  eye: string;
  pupil: string;
  nose: string;
  earInner: string;
  detail: string;
}

